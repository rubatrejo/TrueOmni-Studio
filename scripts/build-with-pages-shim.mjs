#!/usr/bin/env node
/**
 * Wrapper de `next build` que crea un shim transitorio en src/pages/
 * para neutralizar el bug de SSG /404 de Next 15 con App Router.
 *
 * Bug: Next auto-genera un fallback legacy _error/_document que importa
 * <Html> desde next/document, lo que rompe el build SSG en /404 y /500
 * AUNQUE todas las páginas del App Router tengan force-dynamic.
 * Reproducible en 15.5.15 (commit `af4713f` lo diagnosticó por primera vez).
 *
 * Fix: la mera presencia de src/pages/_document.tsx desactiva el fallback
 * auto-generado y deja que el App Router maneje /404 limpiamente.
 *
 * ¿Por qué un wrapper en lugar de commitear src/pages/?
 * Si el shim se commitea, el dev server falla con `ENOENT: open
 * .next/server/pages/_document.js` (Next dev no precompila Pages Router al
 * arranque). Lo aprendimos en `af4713f` → `a212ce8`. El shim solo existe
 * durante `pnpm build`, jamás durante `pnpm kiosk:dev`.
 *
 * Cleanup atómico: try/finally + handlers SIGINT/SIGTERM garantizan que
 * src/pages/ se borre incluso si el build falla, se mata o se interrumpe.
 */

import { spawn } from 'node:child_process';
import { existsSync } from 'node:fs';
import { mkdir, rm, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const pagesDir = path.join(root, 'src', 'pages');

const DOCUMENT_TSX = `// AUTO-GENERATED by scripts/build-with-pages-shim.mjs — DO NOT COMMIT.
// Si ves este archivo en git, el cleanup del wrapper falló. Bórralo.
import { Head, Html, Main, NextScript } from 'next/document';

export default function Document() {
  return (
    <Html lang="en">
      <Head />
      <body>
        <Main />
        <NextScript />
      </body>
    </Html>
  );
}
`;

const ERROR_TSX = `// AUTO-GENERATED by scripts/build-with-pages-shim.mjs — DO NOT COMMIT.
// Si ves este archivo en git, el cleanup del wrapper falló. Bórralo.
import type { NextPageContext } from 'next';

interface ErrorProps {
  statusCode?: number;
}

function ErrorPage({ statusCode }: ErrorProps) {
  return <p>{statusCode ? \`Error \${statusCode}\` : 'An error occurred'}</p>;
}

ErrorPage.getInitialProps = ({ res, err }: NextPageContext) => {
  const statusCode = res?.statusCode ?? err?.statusCode ?? 404;
  return { statusCode };
};

export default ErrorPage;
`;

async function createShim() {
  if (existsSync(pagesDir)) {
    throw new Error(
      'src/pages/ ya existe. El shim no puede arrancar con un pages dir manual presente. ' +
        'Borra src/pages/ o investiga si alguien añadió Pages Router al proyecto.'
    );
  }
  await mkdir(pagesDir, { recursive: true });
  await writeFile(path.join(pagesDir, '_document.tsx'), DOCUMENT_TSX);
  await writeFile(path.join(pagesDir, '_error.tsx'), ERROR_TSX);
}

async function removeShim() {
  if (existsSync(pagesDir)) {
    await rm(pagesDir, { recursive: true, force: true });
  }
}

function runNextBuild() {
  return new Promise((resolve, reject) => {
    const child = spawn('pnpm', ['exec', 'next', 'build'], {
      cwd: root,
      stdio: 'inherit',
      env: process.env,
    });
    child.on('exit', (code) => {
      if (code === 0) resolve();
      else reject(new Error(`next build exited with code ${code}`));
    });
    child.on('error', reject);
  });
}

let cleanedUp = false;
async function safeCleanup() {
  if (cleanedUp) return;
  cleanedUp = true;
  try {
    await removeShim();
  } catch (err) {
    console.error('⚠ Failed to remove src/pages/ shim:', err.message);
  }
}

// Limpieza ante señales (Ctrl+C, kill). Best-effort sync para no perder cleanup.
for (const sig of ['SIGINT', 'SIGTERM', 'SIGHUP']) {
  process.on(sig, async () => {
    console.log(`\n▶ ${sig} recibido — limpiando shim…`);
    await safeCleanup();
    process.exit(130);
  });
}

// En Vercel, NO borramos el shim al final: tras el exit de este script
// Vercel ejecuta su propio tracer (`Traced Next.js server files`) que
// resuelve archivos referenciados en `.next/server/pages/_document.js`
// — si el shim ya no existe, lstat falla y el deploy aborta. Como cada
// build de Vercel clona el repo desde git limpio, dejarlo "sucio" en el
// runner no afecta a builds futuros.
const skipCleanup = process.env.VERCEL === '1';

(async () => {
  let exitCode = 0;
  try {
    console.log('▶ Creando src/pages/ shim (transitorio, solo para build)…');
    await createShim();
    await runNextBuild();
    console.log('✓ Build completado.');
  } catch (err) {
    console.error('✖ Build falló:', err.message);
    exitCode = 1;
  } finally {
    if (skipCleanup) {
      console.log('▶ Saltando cleanup del shim (Vercel runner — los tracers post-build lo necesitan).');
    } else {
      console.log('▶ Borrando src/pages/ shim…');
      await safeCleanup();
    }
  }
  process.exit(exitCode);
})();
